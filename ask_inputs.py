def create_ask_input_init(message):
    """
    This function is used to ask the user for input.

    Parameters:
    - message (str): The message to display when asking for input.

    Returns:
    - int: The user's input as an integer.
    """
    return int(input(message))

def create_ask_input(message):
    """
    This function is used to ask the user for input.

    Parameters:
    - message (str): The message to display when asking for input.

    Returns:
    - str: The user's input.
    """
